def t(key: str, lang: str = "ru") -> str:
    """Simple translation by language code with RU fallback."""
    return MESSAGES.get(lang, MESSAGES["ru"]).get(key, MESSAGES["ru"].get(key, key))


async def T(user_id: int, key: str) -> str:
    """Translate using user's language preference stored in the database."""
    from .db import fetch_user

    user = await fetch_user(user_id)
    lang = user.get("lang") if user else "ru"
    return t(key, lang)
